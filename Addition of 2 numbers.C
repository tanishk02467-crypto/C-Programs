#include <stdio.h>

int main() {
    int num1, num2, sum;

    
    printf("----- Addition of Two Numbers -----\n");
    printf("Enter the first number  : ");
    scanf("%d", &num1);

    printf("Enter the second number : ");
    scanf("%d", &num2);


    sum = num1 + num2;

    
    printf("--\n");
    printf("First Number  : %d\n", num1);
    printf("Second Number : %d\n", num2);
    printf("Sum = %d\n", sum);


}
